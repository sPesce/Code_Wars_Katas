const getTotalBoxes = (unopened,exRate,opened = 0) => {
  const exchanged = Math.floor(unopened/exRate);
  const r = unopened % exRate;
  if(exchanged >= 1)
    return getTotalBoxes(r + exchanged,exRate, opened + (unopened - r));  
  
  else return unopened + opened
}

const boxCalculator = (scenarioStr) => {
  const [budget,price,exRate] = scenarioStr.split(" ");
  const unopened = Math.floor(budget/price);
  return getTotalBoxes(unopened,exRate);
}

